//variable declaration and initialization in C++
// In C++, a variable is a named storage location that can hold a value of a specific type. 
//data types in C++ int, char, float, bool, double are some of the fundamental data types in C++.
#include<iostream>
using namespace std;

int main() {
     int age = 25;
     cout << age << endl;
     
     char grade = 'A';
     cout << grade << endl;
     
     float PI = 3.14f;
     cout << PI << endl;
     
     bool isSafe = true;
     cout << isSafe << endl; //true will be printed as 1 and false will be printed as 0
     
     double price = 19.99;
     cout << price << endl;
    return 0;
}