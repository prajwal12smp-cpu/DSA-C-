// This program demonstrates the use of basic arithmetic operators in C++.

#include<iostream>
using namespace std;

int main()
{
    int a=10, b=20;
    cout<<"a+b="<<a+b<<endl;
    cout<<"a-b="<<a-b<<endl;
    cout<<"a*b="<<a*b<<endl;
    cout<<"a/b="<<a/b<<endl;
    cout<<"a%b="<<a%b<<endl;

    return 0;
}